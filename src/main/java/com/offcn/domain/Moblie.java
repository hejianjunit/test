package com.offcn.domain;

public class Moblie {
	private Integer id;
	private String moblienumber;
	private String mobliearea;
	private String moblietype;
	private String areacode;
	private String postcode;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getMoblienumber() {
		return moblienumber;
	}
	public void setMoblienumber(String moblienumber) {
		this.moblienumber = moblienumber;
	}
	public String getMobliearea() {
		return mobliearea;
	}
	public void setMobliearea(String mobliearea) {
		this.mobliearea = mobliearea;
	}
	public String getMoblietype() {
		return moblietype;
	}
	public void setMoblietype(String moblietype) {
		this.moblietype = moblietype;
	}
	public String getAreacode() {
		return areacode;
	}
	public void setAreacode(String areacode) {
		this.areacode = areacode;
	}
	public String getPostcode() {
		return postcode;
	}
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}
	
}	
